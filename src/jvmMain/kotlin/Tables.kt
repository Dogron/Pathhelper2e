private fun <T> String.toStatisticsTable(tiers: List<StatTier>, parser: (String) -> T) =
    lines().associate { line ->
        val values = line.split(" ")
        values[0].toInt() to tiers.mapIndexed { index, it ->
            it to parser(values[index + 1])
        }.toMap()
    }

